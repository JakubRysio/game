Map::Map() {
    water = Game::loadTexture("../../sprites/water.png");
    grass = Game::loadTexture("../../sprites/grass.png");
    dirt =  Game::loadTexture("../../sprites/dirt.png");

    moveMap.x=0;
    moveMap.y=0;
    mSpeed=0.1;

    loadMap(testMap);

    src.x = 0;
    src.y = 0;
    src.w = 20;
    src.h = 20;

    dest.w = src.w*2;
    dest.h = src.h*2;
    dest.x=0;
    dest.y=0;
}

Map::~Map() = default;

void Map::loadMap(int arr[80][45]) {
    for( int row=0; row< 80; row++){
        for( int col=0; col< 45; col++){
            matrix[row][col]=arr[row][col];
        }
    }
}

void Map::drawMap() {
    int type = 0;

    for( int row=0; row< 32; row++){
        for( int col=0; col< 18; col++){
            type=matrix[row+(int)moveMap.x][col+(int)moveMap.y];
            dest.x=row*40;
            dest.y=col*40;
            switch (type){
                case 0:
                    Game::simpleDraw(water, src, dest);
                    break;
                case 1:
                    Game::simpleDraw(grass, src, dest);
                    break;
                case 2:
                    Game::simpleDraw(dirt, src, dest);
                    break;
            }
        }
    }
}

void Map::moveUp() {
    if(moveMap.y>0) moveMap.y=moveMap.y-mSpeed;
}

void Map::moveDown() {
    if(moveMap.y<90) moveMap.y=moveMap.y+mSpeed;
}

void Map::moveLeft() {
    if(moveMap.x>0)moveMap.x=moveMap.x-mSpeed;
}

void Map::moveRight() {
    if(moveMap.x<160)moveMap.x=moveMap.x+mSpeed;
}
