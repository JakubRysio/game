Map::Map() {
    water = Game::loadTexture("../../sprites/water.png");
    grass = Game::loadTexture("../../sprites/grass.png");
    dirt =  Game::loadTexture("../../sprites/dirt.png");

    loadMap(testMap);

    src.x = 0;
    src.y = 0;
    src.w = 20;
    src.h = 20;

    dest.w = src.w*2;
    dest.h = src.h*2;
    dest.x=0;
    dest.y=0;
}

Map::~Map() = default;

void Map::loadMap(int arr[18][32]) {
    for (int col = 0; col < 18; col++) {
        for (int row = 0; row < 32; row++) {
            matrix[col][row] = arr[col][row];
        }
    }
}

void Map::drawMap() {
    int type = 0;
    for (int col = 0; col < 18; col++) {
        for (int row = 0; row < 32; row++) {
            type = matrix[col][row];
            dest.x = row * 40;
            dest.y = col * 40;
            switch (type) {
                case 0:
                    Game::simpleDraw(water, src, dest);
                    break;
                case 1:
                    Game::simpleDraw(grass, src, dest);
                    break;
                case 2:
                    Game::simpleDraw(dirt, src, dest);
                    break;
            }
        }
    }
}
